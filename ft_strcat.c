#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int l;
	int m;

	l = 0;
	m = 0;
	while (s1[l])
		l++;
	while (s2[m])
	{
		s1[l + m] = s2[m];
		m++;
	}
	s1[l + m] = '\0';
	return (s1);
}
