#include "libft.h"

void	ft_strclr(char *s)
{
	size_t m;
	size_t len;

	m = 0;
	if (!s)
	{
		return ;
	}
	len = ft_strlen(s);
	while (len > m)
	{
		s[m] = '\0';
		m++;
	}
}
