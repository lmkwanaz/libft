#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	int l;

	l = 0;
	if (!s)
		return ;
	while (s[l] != '\0')
	{
		ft_putchar_fd(s[l], fd);
		l++;
	}
}
