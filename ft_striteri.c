#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *s))
{
	unsigned char l;

	l = 0;
	if (!s)
	{
		return ;
	}
	while (s[l] != '\0')
	{
		f(l, &s[l]);
		l++;
	}
}
