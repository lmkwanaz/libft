#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		l;
	size_t	m;

	l = 0;
	m = 0;
	while (s1[l])
		l++;
	while (s2[m] && m < n)
	{
		s1[l + m] = s2[m];
		m++;
	}
	s1[l + m] = '\0';
	return (s1);
}
